package com.xander.java8._03methodRef;

/**
 * Description: 运算类
 *
 * @author Xander
 * datetime: 2020/9/1 0:14
 */
public class Calc {

    /**
     * 加法运算
     *
     * @param x
     * @param y
     * @return
     */
    public int add(int x, int y) {
        return x + y;
    }

    /**
     * 乘法运算
     *
     * @param x
     * @param y
     * @return
     */
    public static long multi(long x, long y) {
        return x * y;
    }
}
